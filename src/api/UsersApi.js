import fetch from 'isomorphic-fetch';

class UsersApi {
  static createUser(user) {
    debugger;
    const request = {
      method: 'POST',
      body: user,
    };


    return fetch('https://private-93240c-oracodechallenge.apiary-mock.com/users', request).then(response => {
      return response.json();
    }).catch(error => {
      return error;
    });
  }
}

export default UsersApi;
